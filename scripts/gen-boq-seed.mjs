// Generates supabase/seed-boq-library.sql from the curated templates in
// lib/boqLibrary.ts so the SQL seed always matches the in-app demo seed.
// Run: node scripts/gen-boq-seed.mjs
import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, "..");
const src = readFileSync(join(root, "lib", "boqLibrary.ts"), "utf8");

// Extract the BOQ_LIBRARY_TEMPLATE_SEEDS array literal (valid JS) and eval it.
const marker = "export const BOQ_LIBRARY_TEMPLATE_SEEDS: TemplateSeed[] =";
const start = src.indexOf(marker);
if (start === -1) throw new Error("template seeds marker not found");
const arrStart = src.indexOf("[", start);
const arrEnd = src.lastIndexOf("];");
const literal = src.slice(arrStart, arrEnd + 1);
// eslint-disable-next-line no-eval
const seeds = eval("(" + literal + ")");

const seedToRow = (seed) => {
  switch (seed[0]) {
    case "header":
      return { id: "", type: "header", itemNo: "", description: seed[1], unit: "", qty: "", rate: "", amount: "" };
    case "item":
      return { id: "", type: "item", itemNo: seed[1], description: seed[2], unit: seed[3], qty: seed[4], rate: "", amount: "" };
    case "subtotal":
      return { id: "", type: "subtotal", itemNo: "", description: seed[1], unit: "", qty: "", rate: "", amount: "0.00" };
    case "grandtotal":
      return { id: "", type: "grandtotal", itemNo: "", description: seed[1], unit: "", qty: "", rate: "", amount: "0.00" };
    case "notes":
      return { id: "", type: "notes", itemNo: "", description: seed[1], unit: "", qty: "", rate: "", amount: "" };
    default:
      throw new Error("unknown row type " + seed[0]);
  }
};

const sql = (s) => s.replace(/'/g, "''");

const lines = [];
lines.push("-- ============================================================================");
lines.push("-- seed-boq-library.sql  — GENERATED by scripts/gen-boq-seed.mjs. Do not edit by hand.");
lines.push("-- Seeds genuine, structured starter BOQ templates into public.boq_library_items.");
lines.push("-- Safe to run in the Supabase SQL editor. Requires the subcategory column");
lines.push("-- (run supabase/add-boq-subcategory.sql first). Re-running replaces the seeded set.");
lines.push("-- ============================================================================");
lines.push("");
lines.push("-- Remove any previously-seeded copies (matched by exact name) so re-runs stay clean.");
const names = seeds.map((t) => `'${sql(t.name)}'`).join(", ");
lines.push(`delete from public.boq_library_items where name in (${names});`);
lines.push("");

for (const t of seeds) {
  const sheets = t.sheets.map((sheet, i) => ({
    id: "",
    project_id: "",
    name: sheet.name,
    sort_order: i,
    rows: sheet.rows.map(seedToRow),
  }));
  const json = JSON.stringify(sheets);
  lines.push(`insert into public.boq_library_items (name, description, category, subcategory, sheets)`);
  lines.push(`values (`);
  lines.push(`  '${sql(t.name)}',`);
  lines.push(`  '${sql(t.description)}',`);
  lines.push(`  '${sql(t.category)}',`);
  lines.push(`  '${sql(t.subcategory)}',`);
  lines.push(`  '${sql(json)}'::jsonb`);
  lines.push(`);`);
  lines.push("");
}

lines.push("select category, subcategory, name from public.boq_library_items order by category, subcategory, name;");
lines.push("");

writeFileSync(join(root, "supabase", "seed-boq-library.sql"), lines.join("\n"), "utf8");
console.log(`Wrote supabase/seed-boq-library.sql with ${seeds.length} templates.`);
